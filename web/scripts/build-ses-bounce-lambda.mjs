/**
 * SES bounce handler Lambda の bundle script (#134)。
 *
 * 出力: `web/dist/lambda/ses-bounce-handler/index.mjs`
 * Terraform の `data.archive_file.ses_bounce_handler` がこのディレクトリを zip 化する。
 *
 * `$lib/...` の SvelteKit エイリアスは esbuild 単体では解決できないため、 同 build 内で
 * tsconfig paths プラグインの代わりに `alias` オプションで `$lib` → `src/lib` を手動 mapping する。
 */
import esbuild from 'esbuild';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { rm, mkdir } from 'node:fs/promises';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const OUT_DIR = resolve(ROOT, 'dist/lambda/ses-bounce-handler');

await rm(OUT_DIR, { recursive: true, force: true });
await mkdir(OUT_DIR, { recursive: true });

await esbuild.build({
	entryPoints: [resolve(ROOT, 'src/lambda/ses-bounce-handler.ts')],
	outfile: resolve(OUT_DIR, 'index.mjs'),
	bundle: true,
	platform: 'node',
	target: 'node22',
	format: 'esm',
	minify: false,
	sourcemap: 'inline',
	alias: {
		// SvelteKit の $lib エイリアスを esbuild 用に手動再現
		$lib: resolve(ROOT, 'src/lib'),
		// `$app/...` 等は本 handler では使わないが、 もし import 経路に混ざったら build を fail させる
	},
	external: [
		// Lambda runtime (node22.x) はこれらを provide しないので bundle に含める。
		// AWS SDK v3 は runtime に **含まれない** (Lambda v3 runtime は v2 SDK のみプリインストール)
		// なので bundle に含める = ここでは external 指定しない。
	],
	banner: {
		// $app/environment などのエイリアスがエラーにならないよう、 importer が居ない handler に
		// import が紛れ込んでも警告で済むようにする (現状の handler では未使用)。
		js: '// generated by web/scripts/build-ses-bounce-lambda.mjs (#134)'
	}
});

console.log(`Bundled: ${OUT_DIR}/index.mjs`);
